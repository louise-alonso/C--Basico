// variaveis
int numero1 = 50;
int numero2 = 10;

//divisao
Console.WriteLine(numero1 / numero2);

//multiplicação
Console.WriteLine(numero1 * numero2);

//soma
Console.WriteLine(numero1 + numero2);

//subtração
Console.WriteLine(numero1 - numero2);

//resto da divisão, modulo
Console.WriteLine(numero1 % numero2);

//incrementador
numero1++;
Console.WriteLine(numero1);

//decrementador
numero2--;
Console.WriteLine(numero2);
 

